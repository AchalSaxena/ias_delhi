<script setup>
import { ref, onMounted, computed, watch } from 'vue';
import { useRoute } from 'vue-router';
import CourseCard from '../components/CourseCard.vue';
import CategoryFilter from '../components/CategoryFilter.vue';
import { useCourseStore } from '../stores/courseStore';
import { useFilterStore } from '../stores/filterStore';
import heroBanner from '../assets/images/banner/2025_Slider.png';

const route = useRoute();
const courseStore = useCourseStore();
const filterStore = useFilterStore();
const loading = ref(false);
const searchQuery = ref('');

const syncFiltersFromRoute = () => {
  if (route.query.category !== undefined) {
    filterStore.setCategory(route.query.category);
  }
  if (route.query.search !== undefined) {
    searchQuery.value = route.query.search;
    filterStore.setSearchQuery(route.query.search);
  } else {
    searchQuery.value = filterStore.searchQuery || '';
  }
};

onMounted(async () => {
  syncFiltersFromRoute();
  loading.value = true;
  try {
    await courseStore.fetchCourses();
  } finally {
    loading.value = false;
  }
});

// Watch query parameters for deep linking
watch(
  () => route.query,
  () => {
    syncFiltersFromRoute();
  },
  { deep: true }
);

// Sync search query changes to filterStore
watch(searchQuery, (newVal) => {
  filterStore.setSearchQuery(newVal);
});

// Sync filterStore changes back to search input
watch(() => filterStore.searchQuery, (newVal) => {
  searchQuery.value = newVal || '';
});

const filteredCourses = computed(() => {
  let courses = courseStore.courses;

  if (filterStore.selectedCategory) {
    courses = courses.filter(c => c.category === filterStore.selectedCategory);
  }

  if (searchQuery.value) {
    const query = searchQuery.value.toLowerCase();
    courses = courses.filter(c =>
      c.name.toLowerCase().includes(query) ||
      c.description.toLowerCase().includes(query)
    );
  }

  return courses;
});
</script>

